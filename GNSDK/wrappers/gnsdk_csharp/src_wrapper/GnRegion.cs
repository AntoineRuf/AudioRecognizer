
namespace GracenoteSDK {

/**
* Regions used when specifying which locale or list is loaded.
*/
public enum GnRegion {
/** Default region. A region will be selected based on what's available. */
  kRegionDefault = 0,
/** Global region */
  kRegionGlobal,
/** United States region */
  kRegionUS,
/** Japan region */
  kRegionJapan,
/** China region */
  kRegionChina,
/** Taiwan region */
  kRegionTaiwan,
/** Korea region */
  kRegionKorea,
/** Europe region */
  kRegionEurope,
/** Deprecated, will be removed in a future release, use kRegionUS. */
  kRegionNorthAmerica,
/** Latin America region */
  kRegionLatinAmerica,
/** India region */
  kRegionIndia
}

}
